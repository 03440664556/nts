                        <h2>Frequently asked question</h2>
                        <!-- accordions -->
                        <div class="accordions">
                            <!-- content-title -->
                            <div class="content-title active">From Where Can I Get the Information About the NTS™ Tests?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">You can get the information about NTS™ tests from any of the following sources: NTS™ website and advertisements through Print Media.</div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">From Where Can I Get the Application Forms?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">You can get Application Forms from NTS™ website i.e., <a href="http://www.nts.org.pk">http://www.nts.org.pk</a>. However, ensure that you get these before the last date for registration.</div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">How Can I Register for NTS™ Test?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">Fill your application form according to the given instructions . Submit it by paying the online Test Fee in any branch of UBL, ABL, MCB or HBL.</div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">How Shall I Fill the Forms?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">A few things must be clear to you before you fill in the form. Any incomplete or ambiguous information in the form may cause delay in the registration process and you may not be able to take the test on time. The instructions to fill the form are as follows:
                                <ol>
                                    <li>If application form is in type-able form, fill application form and print it. Otherwise use black ink and BLOCK LETTERS to fill out the form.</li>
                                    <li>Download the appropriate test application form.</li>
                                    <li>Be careful with the spellings of your name.</li>
                                    <li>Write the correspondence / mailing address correctly where you can be contacted in future.</li>
                                    <li>Test Fee is submitted in the form of Online Deposit Slip in any nearest UBL, ABL, MCB or HBL branch, NTS™ Copy (Original) must be attached with the Application Form.</li>
                                    <li>Also indicate your choice for the city, where you want to take the test out of the lis available to you.</li>
                                    <li>Two passport size photographs are affixed at appropriate places on form. (Use gum instead of using staples)</li>
                                    <li>Original Bank Deposit Slip (NTS™ Copy)</li>
                                    <li>Original CNIC / Valid Passport (Photo Copy)</li>
                                    <li>Fulfill other requirement, if any.</li>
                                </ol>
                            </div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">Where and how to Submit the Form?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">After you fill the form according to the instructions, send your form through any courier service or mail (Post office). Make sure to attach:
                                <ol>
                                    <li>Two copies of your recent photographs</li>
                                    <li>Clearly written candidate’s name in the BLOCK LETTERS</li>
                                    <li>Clearly written candidates father’s name in BLOCK LETTERS</li>
                                    <li>Clearly written National Identity Card (NIC) number</li>
                                    <li>Clearly written candidate’s postal address in BLOCK LETTERS</li>
                                    <li>Clearly written phone number etc.</li>
                                    <li>Fulfill other requirement, if any.</li>
                                </ol>
                                If any essential requirement is missing in the Registration Form, it will be clearly marked as rejected.
                            </div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">Does NTS™ Confirm My Registration?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">After submitting the Application Form you will get your Roll Number Slip (Call Letter) containing Roll No., Test Venue, etc, on your mailing address. This slip confirms your registration. You can also visit NTS™ website for confirmation. You must take that slip with you to the test center on the day of your test otherwise you are not allowed to sit in the Examination Hall. You may also take the print of your information from NTS website. In case of any confusion or problems with the registration process you can Contact NTS™ Office.</div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">May I Register on the Day of Examination?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">The registration of candidates on the day of NTS™ Examination cannot be entertained. The candidate should get registered before the deadline, announced by NTS™ for the close of registration process.</div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">How Can I Get Help in Case of Any Problem During Registration Process?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">If you have any confusion about the registration process you can clarify by calling the NTS™ Office or Contact (mail,email,fax) NTS™ Office.</div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">How Can I Get the Test Schedule?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">If you have any confusion about the Test Schedule you can get the details from the NTS™ website.</div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">How to Find the Place and Schedule of Test?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">The schedule, time, location, etc., is uploaded on NTS™ website i.e., <a href="http://www.nts.org.pk">http://www.nts.org.pk</a> from where you can get further help and other informations.</div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">How to Get Myself Identified at the Test Center?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">You have to present your original Roll No. Slip and original CNIC / Valid Passport. The staff at the Test Center identifies you with the photograph printed on the slip and from your original CNIC / Valid Passport. (Less than 18 years of age candidates must bring Original B. Form and SSC Certificate with photograph at the test center). Along With original Roll No. Slip, any one of following document is required.
                                <ol>
                                    <li>Original CNIC</li>
                                    <li>Original Passport</li>
                                    <li>Original B.Form (In case if candidate is less than 18 years of age)</li>
                                    <li>Original NADRA token slip with any Valid Government document having photograph.</li>
                                </ol>
                            </div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">What Should I Take With Me to the Test Center?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">The candidate should take sharpened pencils, pen with eraser and clip-board. Besides this the candidate is also asked to show his/her Roll No. Slip and original CNIC / Valid Passport. (Less than 18 years of age candidates must bring Original B. Form and SSC Certificate with photograph at the test center).</div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">What are the Things that I Should Not Take With Me in the Test Center?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">Cheating material or anything which is prohibited by NTS is strictly not allowed to be taken inside the test center. If you are found with any of these items with you during the test you are not allowed to take the examination and be considered disqualified.</div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">Where Shall I Sit?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">Enter the examination hall and look for the seat that has your roll number pasted on it. The seats are arranged in a sequential order of roll numbers. If you do not find your seat, ask any of the invigilators at the center.</div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">What are the Rules and Regulations that are applied to me inside the Test Center?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">The test is given on the day and at the time scheduled. You are asked to observe punctuality.
                                <ol>
                                    <li>You are assigned a seat by the invigilation staff.</li>
                                    <li>Cheating material or anything which is prohibited by NTS is strictly not allowed to be taken inside the test center. If you are found with any of these items with you during the test you are not allowed to take the examination and be considered disqualified.</li>
                                    <li>You are not allowed to eat, drink, or use tobacco during the test.</li>
                                    <li>If you do not show the Roll No. Slip and original CNIC / Valid Passport, and your photograph does not match, you are not permitted to take the test.</li>
                                    <li>No discussion or communication with the fellow candidates is allowed during the test session.</li>
                                    <li>You are also not allowed to leave Test Center without the permission of the supervisor.</li>
                                    <li>Under no circumstances the test questions or any part of a test be removed, reproduced or disclosed verbally, electronically, or physically or by any other means to any person or entity.</li>
                                    <li>Test centers do not have large waiting areas. Friends or relatives who accompany you to the test center are not permitted to wait in the near vicinity of the test center or contact you while you are taking the test.</li>
                                    <li>If you need to leave your seat at any time during the test, raise your hand and ask the invigilator. Timing of the section does not stop.</li>
                                    <li>Repeated unscheduled breaks will be documented and reported to NTS™.</li>
                                    <li>NTS™ reserves the right to videotape any or all of the testing sessions.</li>
                                    <li>Dress in such a way that you can adapt to any room temperature.</li>
                                    <li>You cannot communicate with anyone (other than the test center staff) while the test session is in progress or even during the breaks.</li>
                                    <li>You are not permitted to leave the test center vicinity during the test session or breaks.</li>
                                    <li>If at any time during the test you believe that you have a problem with your test, or need the Invigilation Staff for any reason, raise your hand to notify the Invigilation Staff.</li>
                                </ol>
                            </div>
                            <!--/accordions content -->
                            <!-- content-title -->
                            <div class="content-title">On What Basis Can I be disqualified from the Test?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">If you are found indulged in any of the following activities, he / she is disqualified from the test:
                                <ol>
                                    <li>Arriving late at the test center.</li>
                                    <li>Taking the test for someone else.</li>
                                    <li>Failing to provide the Roll No. Slip.</li>
                                    <li>Failing to provide original CNIC / Valid Passport, (Less than 18 years of age candidates must bring Original B.Form and SSC Certificate with photograph at the test center).</li>
                                    <li>Using any objectionable material during the test.</li>
                                    <li>Trying to create disturbance inside the test center.</li>
                                    <li>Showing ill behavior towards the invigilation staff.</li>
                                    <li>Trying to communicate with the fellow candidates while the test is in progress.</li>
                                    <li>Trying to tear pages from the question paper.</li>
                                    <li>Showing indiscipline i.e. eating, smoking etc.</li>
                                    <li>Leaving the test center without permission.</li>
                                </ol>
                            </div>
                            <!-- content-title -->
                            <div class="content-title">How Shall I Take Test?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">Sit on your seat and wait for the time of the test. Make sure that you do not have any objectionable material. The invigilators will give you the question book and answer sheets. Fill in the essential information in the blank columns of answer sheet. While attempting to answer questions read the choices in each question and mark the corresponding space on the answer sheet. Do keep the time aspect in mind. You would not be given extra time.</div>
                            <!-- content-title -->
                            <div class="content-title">How Shall I Mark the Answer Sheet?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">
                                <ol>
                                    <li>Write Your Name, Father’s Name, Roll Number etc (if required) on the answer sheet.</li>
                                    <li>Read the directions carefully before you begin your test.</li>
                                    <li>Each question has maximum five choices and minimum two. You are required to choose the correct choice for every question and fill the corresponding oval with a lead pencil on the answer sheet.</li>
                                    <li>The invigilator announces the procedure of marking the answer sheets. Listen carefully to the instructions. When you mark the answer sheets, make sure that the space is completely filled in.</li>
                                    <li>Use a lead pencil or Pens for marking the Answer Sheets.</li>
                                    <li>Do not mark more than one answer.</li>
                                    <li>If a question is found with no answer or it has more than one marked choice, it is not considered for marking.</li>
                                </ol>
                            </div>
                            <!-- content-title -->
                            <div class="content-title">What are the Other Test Taking Strategies?</div>
                            <!--/content-title -->
                            <!-- accordions content -->
                            <div class="content">
                                <ol>
                                    <li>You are provided with a few blank pages in the test paper. You can use them for your rough work.</li>
                                    <li>Keep a very careful track of time.</li>
                                    <li>Do not panic while solving your paper.</li>
                                    <li>Do not make any extra marks on the answer sheet. Computer might register these and consider them as another marked choice.</li>
                                </ol>
                            </div>
